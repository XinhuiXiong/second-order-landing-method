def _example_brockett(seed: int = 0) -> None:  # pragma: no cover
    """Small Brockett example: f(X)=0.5 tr(X^T B X C), with symmetric B,C."""
    rng = np.random.default_rng(seed)
    n, p = 200, 20
    B = rng.standard_normal((n, n)); B = sym(B)
    C = rng.standard_normal((p, p)); C = sym(C)

    def cost_f(X: Array) -> float:
        return 0.5 * float(np.trace(X.T @ B @ X @ C))

    def grad_f(X: Array) -> Array:
        # ∇f = B X C
        return B @ X @ C

    def hess_f(X: Array, V: Array) -> Array:
        # Hessian action: B V C
        return B @ V @ C

    solver = ExPenSecondOrderNewton(beta=1e2, tol=1e-8, max_iter=100, verbosity=1)
    res = solver.run(n=n, p=p, cost_f=cost_f, grad_f=grad_f, hess_f=hess_f, seed=seed)
    print(res.stopping_reason, res.iterations)


