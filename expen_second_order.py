Array = np.ndarray


def sym(A: Array) -> Array:
    """Symmetrization operator Φ(A) = (A + A^T)/2."""
    return 0.5 * (A + A.T)


def expen_map_A(XTX: Array) -> Array:
    """A(X) := (3/2) I - (1/2) X^T X."""
    p = XTX.shape[0]
    return 1.5 * np.eye(p) - 0.5 * XTX


def expen_Y(X: Array) -> Tuple[Array, Array, Array]:
    """Return (Y, XTX, A) where Y = X A and A = 3/2 I - 1/2 X^T X."""
    XTX = X.T @ X
    A = expen_map_A(XTX)
    Y = X @ A
    return Y, XTX, A


def JX_action(X: Array, D: Array, *, XTX: Optional[Array] = None, A: Optional[Array] = None) -> Array:
    """Jacobian action J_X(D) of the mapping X \mapsto X(3/2 I - 1/2 X^T X).

    J_X(D) = D(3/2 I - 1/2 X^T X) - X Φ(X^T D).
    """
    if XTX is None:
        XTX = X.T @ X
    if A is None:
        A = expen_map_A(XTX)
    return D @ A - X @ sym(X.T @ D)


