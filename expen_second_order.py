"""Second-order ExPen solver for orthogonality-constrained problems on the Stiefel manifold.

This module implements a **second-order (inexact) Newton–Krylov method** for minimizing the
"exponential-penalty" objective (ExPen) introduced by Xiao & Liu (2022):

    min_{X \in R^{n\times p}}  h(X) := f( X(\tfrac32 I_p - \tfrac12 X^\top X) )
                               + \tfrac\beta4 \|X^\top X - I_p\|_F^2.

It is intended to be structurally similar to the provided `second_order_landing.py`:
- explicit gradient and Hessian-vector product (HVP)
- a Krylov linear solver (GMRES) for the Newton equation
- an inexact forcing rule (Eisenstat–Walker style) for the inner solve
- simple globalization via Armijo backtracking

You provide the original objective oracles:
    cost_f(X) -> scalar
    grad_f(X) -> (n,p) array
    hess_f(X, V) -> (n,p) array  (Hessian action / HVP of f at X applied to V)

References (formulas):
- Proposition 2.8 (gradient of h)
- Proposition 2.9 (HVP / Hessian action of h)

Dependencies:
    numpy, scipy (scipy.sparse.linalg.gmres, LinearOperator)

Author: generated by ChatGPT for the user's project.
"""

